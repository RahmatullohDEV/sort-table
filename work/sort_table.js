//counter 
  function totalClick(click) {
    const totalClicks = document.getElementById('totalClicks');
    const currentValue = parseInt(totalClicks.innerText);

    if (click === 0) {
      totalClicks.innerText = 0;
    } else {
      const newValue = currentValue + click;
      if (newValue < 0) {
        totalClicks.innerText = 0;
      } else {
        totalClicks.innerText = newValue;
      }
    }
  }
